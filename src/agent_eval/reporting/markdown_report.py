"""Markdown report formatter for evaluation results.

Generates GitHub-flavored markdown suitable for PR comments and
documentation.
"""
from __future__ import annotations

from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from agent_eval.core.report import EvalReport


class MarkdownReportFormatter:
    """Renders an EvalReport as GitHub-flavored Markdown."""

    def render(self, report: EvalReport) -> str:
        """Render the report as a Markdown string."""
        lines: list[str] = []

        status = "PASS" if report.all_passed else "FAIL"
        lines.append(f"# Evaluation Report: {report.suite_name}")
        lines.append("")
        lines.append(f"**Agent:** {report.agent_name}")
        lines.append(f"**Status:** {status}")
        lines.append(f"**Timestamp:** {report.timestamp}")
        lines.append("")

        # Summary table
        lines.append("## Summary")
        lines.append("")
        lines.append("| Metric | Value |")
        lines.append("|--------|-------|")
        lines.append(f"| Total Cases | {report.total_cases} |")
        lines.append(f"| Total Runs | {report.total_runs} |")
        lines.append(f"| Passed | {report.passed_count} |")
        lines.append(f"| Failed | {report.failed_count} |")
        pass_rate = round(report.pass_rate * 100, 1) if report.total_runs > 0 else 0.0
        lines.append(f"| Pass Rate | {pass_rate}% |")
        lines.append("")

        # Dimension scores
        if report.dimension_means:
            lines.append("## Dimension Scores")
            lines.append("")
            lines.append("| Dimension | Mean Score |")
            lines.append("|-----------|-----------|")
            for dim, score in report.dimension_means.items():
                score_pct = round(score * 100, 1)
                lines.append(f"| {dim.value.title()} | {score_pct}% |")
            lines.append("")

        # Per-case results (collapsed for large suites)
        if report.results:
            lines.append("## Results")
            lines.append("")
            if len(report.results) > 20:
                lines.append("<details>")
                lines.append(f"<summary>Show all {len(report.results)} results</summary>")
                lines.append("")

            lines.append("| Case | Run | Score | Status |")
            lines.append("|------|-----|-------|--------|")
            for r in report.results:
                status_emoji = "pass" if r.passed else "FAIL"
                score = round(r.overall_score * 100, 1)
                lines.append(
                    f"| {r.case_id} | {r.run_index} | {score}% | {status_emoji} |"
                )

            if len(report.results) > 20:
                lines.append("")
                lines.append("</details>")

        lines.append("")
        lines.append("---")
        lines.append("*Generated by [agent-eval](https://github.com/aumos-ai/agent-eval)*")
        lines.append("")

        return "\n".join(lines)
